function channelMap = getChannelMap(band)
    %getChannelMap Returns valid channel numbers in all possible bandwidths in
    %a band as per Annex E of IEEE Std 802.11-2020, IEEE Std 802.11ax-2021 and
    %IEEE P802.11be/D5.0
    %
    %   Note: This is an internal undocumented function and its API and/or
    %   functionality may change in subsequent releases.
    %
    %   CHANNELMAP = getValidChannelNums(BAND) returns valid channel numbers in
    %   all possible bandwidths in a band as per Annex E.
    %
    %   CHANNELMAP is a cell array where first row indicates the bandwidth that
    %   the channel numbers in corresponding column belong to. Bandwidth is
    %   indicated by character vectors '20 MHz','40 MHz','80 MHz','160
    %   MHz','320 MHz'. Number of columns depends on the bandwidths allowed in
    %   the band. First column starting from the second row represents the 20
    %   MHz channel numbers. Second, third, fourth and fifth columns represent
    %   the 40, 80, 160 and 320 MHz channel numbers that contain the 20 MHz
    %   channels in first column.
    %
    %   BAND is scalar with one of the following values: 2.4, 5, 6

    %   Copyright 2024-2025 The MathWorks, Inc.

    switch band
        case 2.4
            channelMap = {...
                '20 MHz', '40 MHz'; ...
                [1,5], 3; ...
                [2,6], 4; ...
                [3,7], 5; ...
                [4,8], 6; ...
                [5,9], 7; ...
                [6,10], 8; ...
                [7,11], 9; ...
                [8,12], 10; ...
                [9,13], 11};
        case 5
            channelMap = {...
                '20 MHz','40 MHz','80 MHz','160 MHz'; ...
                [36 40], 38, 42, 50; ...
                [44 48], 46, 42, 50; ...
                [52 56], 54, 58, 50; ...
                [60 64], 62, 58, 50; ...
                [100 104], 102, 106, 114; ...
                [108 112], 110, 106, 114; ...
                [116 120], 118, 122, 114; ...
                [124 128], 126, 122, 114; ...
                [132 136], 134, 138, 0; ...
                [140 144], 142, 138, 0; ...
                [149 153], 151, 155, 163; ...
                [157 161], 159, 155, 163; ...
                [165 169], 167, 171, 163; ...
                [173 177], 175, 171, 163};
        case 6
            channelMap = {...
                '20 MHz','40 MHz','80 MHz','160 MHz','320 MHz'; ...
                2, 0, 0, 0, 0
                [1 5], 3, 7, 15, 31; ...
                [9, 13], 11, 7, 15, 31; ...
                [17 21], 19, 23, 15, 31; ...
                [25 29], 27, 23, 15, 31; ...
                [33 37], 35, 39, 47, [31 63]; ...
                [41 45], 43, 39, 47, [31 63]; ...
                [49 53], 51, 55, 47, [31 63]; ...
                [57 61], 59, 55, 47, [31 63]; ...
                [65 69], 67, 71, 79, [63 95]; ...
                [73 77], 75, 71, 79, [63 95]; ...
                [81 85], 83, 87, 79, [63 95]; ...
                [89 93], 91, 87, 79, [63 95]; ...
                [97 101], 99, 103, 111, [95 127]; ...
                [105 109], 107, 103, 111, [95 127]; ...
                [113 117], 115, 119, 111, [95 127]; ...
                [121 125], 123, 119, 111, [95 127]; ...
                [129 133], 131, 135, 143, [127 159]; ...
                [137 141], 139, 135, 143, [127 159]; ...
                [145 149], 147, 151, 143, [127 159]; ...
                [153 157], 155, 151, 143, [127 159]; ...
                [161 165], 163, 167, 175, [159 191]; ...
                [169 173], 171, 167, 175, [159 191]; ...
                [177 181], 179, 183, 175, [159 191]; ...
                [185 189], 187, 183, 175, [159 191]; ...
                [193 197], 195, 199, 207, 191; ...
                [201 205], 203, 199, 207, 191; ...
                [209 213], 211, 215, 207, 191; ...
                [217 221], 219, 215, 207, 191; ...
                [225 229], 227, 0, 0, 0; ...
                233, 0, 0, 0, 0};
    end
end

function ind = ehtRUSubcarrierIndices(chanBW,varargin)
%ehtRUSubcarrierIndices RU subcarrier indices as per IEEE P802.11be/D5.0, Section 36.3.2.1
%
%   Note: This is an internal undocumented function and its API and/or
%   functionality may change in subsequent releases.
%
%   IND = ehtRUSubcarrierIndices(CHANBW,RUSIZE) returns a matrix containing
%   the indices for all RUs. Each column contains the indices for an RU.
%
%   CHANBW is the channel bandwidth and must be 20, 40, 80, 160, or 320.
%
%   RUSIZE is the RU size in subcarriers and must be one of 26, 52, 106,
%   242, 484, 968, 996, 2*996, or 4*996.
%
%   IND = ehtRUSubcarrierIndices(CHANBW) returns a matrix containing the
%   indices for all RUs assuming a full bandwidth allocation.
%
%   IND = ehtRUSubcarrierIndices(CHANBW,RUSIZE,RUINDEX) optionally returns
%   the indices for only the RU specified by RUINDEX. When RUSIZE and
%   RUINDEX are vectors IND is the subcarriers for all RUs.

%   Copyright 2022-2024 The MathWorks, Inc.

%#codegen

if nargin>1
    ruSize = varargin{1};
else
    ruSize = wlan.internal.heFullBandRUSize(chanBW);
    ind = singleRUInd(chanBW,ruSize);
    return
end

if isscalar(ruSize)
    if nargin>2
        ind = singleRUInd(chanBW,ruSize(1),varargin{2}(1)); % Index scalar to be propagated for codegen
    else
        ind = singleRUInd(chanBW,ruSize(1));
    end
else
    if nargin>2
        ruIndex = varargin{2}; % Expect RU index when vector of RU sizes
    else
        ruIndex = 1:numel(ruSize); % Default
    end
    ind = zeros(0,1);
    for mru = 1:numel(ruSize)
        ind = [ind; singleRUInd(chanBW,ruSize(mru),ruIndex(mru))]; %#ok<AGROW>
    end
end
end

function ind = singleRUInd(chanBW,ruSize,varargin)
    % IEEE Std 802.11ax-2021, Table 27-5/6/7 - Subcarrier indices for RUs in a 20/40/80 MHz HE PPDU
    switch chanBW
        case {20,40}
            ruSizeInd = wlan.internal.heRUSubcarrierIndices(chanBW,ruSize);
        case 80
            ruSizeInd = cbw80Size(ruSize);
        case 160
           ruSizeInd = cbw160Size(ruSize);
        otherwise
           assert(chanBW==320)
           ruSizeInd = cbw320Size(ruSize);
    end
    if nargin>2
        % Return indices for RU index of interest
        ruIndex = varargin{1};
        ind = ruSizeInd(:,ruIndex);
    else
        % Return indices for all RU indices
        ind = ruSizeInd;
    end
end


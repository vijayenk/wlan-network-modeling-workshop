function sym = symbolMap(numBPSCS)
% symbolMap WLAN QAM symbol mapping
%
%   Note: This is an internal undocumented function and its API and/or
%   functionality may change in subsequent releases.
%
%   SYM = symbolMap(NUMBPSCS) returns the symbol mapping of the WLAN QAM
%   based constellation given by NUMBPSCS. NUMPBSCS is the number of coded
%   bits per subcarrier per spatial stream and it can be equal to 2, 4, 6,
%   8, 10, or 12.

%   Copyright 2017-2023 The MathWorks, Inc.

%#codegen

switch numBPSCS
    case 2 % QPSK
        % As per IEEE Std 802.11-2020 Section 17.3.5.8
        sym = [1; 0; 3; 2];
    case 4 % 16QAM
        % As per IEEE Std 802.11-2020 Section 17.3.5.8
        sym = [2; 3; 1; 0; 6; 7; 5; 4; 14; 15; 13; 12; 10; 11; 9; 8];
    case 6 % 64QAM
        % As per IEEE Std 802.11-2020 Section 17.3.5.8
        sym = [4 5 7 6 2 3 1 0  12 13 15 14 10 11 9 8 ...
            28 29 31 30 26 27 25 24  20 21 23 22 18 19 17 16 ...
            52 53 55 54 50 51 49 48  60 61 63 62 58 59 57 56 ...
            44 45 47 46 42 43 41 40  36 37 39 38 34 35 33 32].';
    case 8 % 256QAM
        % As per IEEE Std 802.11-2020 Section 21.3.10.9
        firstCol = [8 9 11 10 14 15 13 12 4 5 7 6 2 3 1 0].';
        allQuads = [firstCol firstCol+16 firstCol+48 firstCol+32 ...
            firstCol+96 firstCol+112 firstCol+80 firstCol+64 ...
            firstCol+192 firstCol+208 firstCol+240 firstCol+224 ...
            firstCol+160 firstCol+176 firstCol+144 firstCol+128];
        sym = allQuads(:);
    case 10 % 1024QAM
        % As per IEEE Std 802.11ax-2021 Section 27.3.12.9.
        firstCol = [16 17 19 18 22 23 21 20 28 29 31 30 26 27 25 24 ...
            8 9 11 10 14 15 13 12 4 5 7 6 2 3 1 0].';
        allQuads = [firstCol firstCol+32 firstCol+96 firstCol+64 ...
            firstCol+192 firstCol+224 firstCol+160 firstCol+128 ...
            firstCol+384 firstCol+416 firstCol+480 firstCol+448 ...
            firstCol+320 firstCol+352 firstCol+288 firstCol+256 ...
            firstCol+768 firstCol+800 firstCol+864 firstCol+832 ...
            firstCol+960 firstCol+992 firstCol+928 firstCol+896 ...
            firstCol+640 firstCol+672 firstCol+736 firstCol+704 ...
            firstCol+576 firstCol+608 firstCol+544 firstCol+512];
        sym = allQuads(:);
    otherwise % 4096QAM
        % As per IEEE P802.11be/D4.0 Section 36.3.13.7
        firstCol = [32 33 35 34 38 39 37 36 44 45 47 46 42 43 41 40 56 ...
            57 59 58 62 63 61 60 52 53 55 54 50 51 49 48 16 17 19 18 22 ...
            23 21 20 28 29 31 30 26 27 25 24 8 9 11 10 14 15 13 12 4 5 ...
            7 6 2 3 1 0].';
        allQuads = [firstCol firstCol+64 firstCol+192 firstCol+128 ...
            firstCol+384  firstCol+448  firstCol+320  firstCol+256 ...
            firstCol+768  firstCol+832  firstCol+960  firstCol+896 ...
            firstCol+640  firstCol+704  firstCol+576  firstCol+512 ...
            firstCol+1536 firstCol+1600 firstCol+1728 firstCol+1664 ...
            firstCol+1920 firstCol+1984 firstCol+1856 firstCol+1792 ...
            firstCol+1280 firstCol+1344 firstCol+1472 firstCol+1408 ...
            firstCol+1152 firstCol+1216 firstCol+1088 firstCol+1024 ...
            firstCol+3072 firstCol+3136 firstCol+3264 firstCol+3200 ...
            firstCol+3456 firstCol+3520 firstCol+3392 firstCol+3328 ...
            firstCol+3840 firstCol+3904 firstCol+4032 firstCol+3968 ...
            firstCol+3712 firstCol+3776 firstCol+3648 firstCol+3584 ...
            firstCol+2560 firstCol+2624 firstCol+2752 firstCol+2688 ...
            firstCol+2944 firstCol+3008 firstCol+2880 firstCol+2816 ...
            firstCol+2304 firstCol+2368 firstCol+2496 firstCol+2432 ...
            firstCol+2176 firstCol+2240 firstCol+2112 firstCol+2048];
        sym = allQuads(:);
end
end